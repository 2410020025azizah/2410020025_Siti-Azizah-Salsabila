    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(cmbPelanggan.getSelectedIndex()==-1 ||
            cmbMenu.getSelectedIndex()==-1 ||
            txtJumlah.getText().isEmpty() ||
            txtTotal.getText().isEmpty() ||
            cmbStatus.getSelectedIndex()==-1){

             JOptionPane.showMessageDialog(this,"Data masih kosong");
             return;

         }

        try{

            if(idPesanan==0){
                String sql="INSERT INTO pesanan(id_pelanggan,id_menu,jumlah,total,status) VALUES(?,?,?,?,?)";

                PreparedStatement ps=con.prepareStatement(sql);

                ps.setInt(1,idPelanggan.get(cmbPelanggan.getSelectedIndex()));
                ps.setInt(2,idMenu.get(cmbMenu.getSelectedIndex()));
                ps.setInt(3,Integer.parseInt(txtJumlah.getText()));
                ps.setDouble(4,Double.parseDouble(txtTotal.getText()));
                ps.setString(5,cmbStatus.getSelectedItem().toString());

                ps.executeUpdate();

                JOptionPane.showMessageDialog(this,"Data berhasil disimpan");

            }else{

                String sql="UPDATE pesanan SET id_pelanggan=?,id_menu=?,jumlah=?,total=?,status=? WHERE id_pesanan=?";

                PreparedStatement ps=con.prepareStatement(sql);

                ps.setInt(1,idPelanggan.get(cmbPelanggan.getSelectedIndex()));
                ps.setInt(2,idMenu.get(cmbMenu.getSelectedIndex()));
                ps.setInt(3,Integer.parseInt(txtJumlah.getText()));
                ps.setDouble(4,Double.parseDouble(txtTotal.getText()));
                ps.setString(5,cmbStatus.getSelectedItem().toString());
                ps.setInt(6,idPesanan);

                ps.executeUpdate();

                JOptionPane.showMessageDialog(this,"Data berhasil diubah");

            }

            new PesananTampilFrame().setVisible(true);
            dispose();

        }catch(Exception e){

            JOptionPane.showMessageDialog(this,e.getMessage());

        }
    }//GEN-LAST:event_btnSimpanActionPerformed

