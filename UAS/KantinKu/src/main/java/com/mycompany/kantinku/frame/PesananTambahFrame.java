    private void loadPelanggan(){
        cmbPelanggan.removeAllItems();
        idPelanggan.clear();

        try{

            String sql="SELECT * FROM pelanggan";

            PreparedStatement ps=con.prepareStatement(sql);

            ResultSet rs=ps.executeQuery();

            while(rs.next()){

                idPelanggan.add(rs.getInt("id_pelanggan"));
                cmbPelanggan.addItem(rs.getString("nama_pelanggan"));

            }

        }catch(SQLException e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
    
    private void loadMenu(){
        cmbMenu.removeAllItems();
        idMenu.clear();

        try{

            String sql="SELECT * FROM menu";

            PreparedStatement ps=con.prepareStatement(sql);

            ResultSet rs=ps.executeQuery();

            while(rs.next()){

                idMenu.add(rs.getInt("id_menu"));
                cmbMenu.addItem(rs.getString("nama_menu"));
            }

        }catch(SQLException e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
    
    private void hitungHarga(){
        if(cmbMenu.getSelectedIndex()==-1){
            return;
        }

        try{

            String sql="SELECT harga FROM menu WHERE id_menu=?";

            PreparedStatement ps=con.prepareStatement(sql);

            ps.setInt(1,idMenu.get(cmbMenu.getSelectedIndex()));

            ResultSet rs=ps.executeQuery();

            if(rs.next()){

                double harga=rs.getDouble("harga");

                int jumlah = 0;

            try{
                jumlah = Integer.parseInt(txtJumlah.getText());
            }catch(Exception e){
                txtTotal.setText("0");
                return;
            }

                txtTotal.setText(String.valueOf(harga*jumlah));

            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
    
